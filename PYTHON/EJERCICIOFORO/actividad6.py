'''Extraer subcadenas usando slicing (rebanado de cadenas sin usar listas).'''

cadena = "Los ejercicios de Python me traen de cabeza" 
print(cadena[0:3]) # Los 
print(cadena[4:14]) # ejercicios
print(cadena[15:18]) # de 
print(cadena[18:25]) # Python
print(cadena[25:28]) # me 
print(cadena[28:34]) # traen
print(cadena[34:37]) # de
print(cadena[37:44]) # cabeza 