'''Concatenar caracteres o cadenas utilizando el operador + para formar nuevas cadenas.'''
cadena1 = "Nerea"
cadena2 = "Fernandez"
cadena3 = cadena1 + " " + cadena2
print(cadena3)  # Saldría: Nerea Fernandez
cadena4 = "hace ejercicios de Python"
cadena5 = cadena3 + " - " + cadena4
print(cadena5)  # Saldría: Nerea Fernandez - hace ejercicios de Python